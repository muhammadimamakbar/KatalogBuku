package com.imam2trk.katalogbuku;

public class Buku {
    private int fotoBuku;
    private String judulBuku;
    private String penulisBuku;

    public int getFotoBuku() {
        return fotoBuku;
    }

    public void setFotoBuku(int fotoBuku) {
        this.fotoBuku = fotoBuku;
    }

    public String getJudulBuku() {
        return judulBuku;
    }

    public void setJudulBuku(String judulBuku) {
        this.judulBuku = judulBuku;
    }

    public String getPenulisBuku() {
        return penulisBuku;
    }

    public void setPenulisBuku(String penulisBuku) {
        this.penulisBuku = penulisBuku;
    }

    public String getHargaBuku() {
        return hargaBuku;
    }

    public void setHargaBuku(String hargaBuku) {
        this.hargaBuku = hargaBuku;
    }

    private String hargaBuku;


}
